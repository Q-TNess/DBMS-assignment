-- Creating a database called salesDB in Command prompt you type 
--the following command to log into MySQL
mysql -u root -p

--use your MySQL root password when prompted

-- Then create the database by typing the following as is
CREATE DATABASE salesDB;

-- To test it run the query in command prompt  by plugging the following
SHOW DATABASES;

--Follow the same steps to delete the DB but use DROP instead of CREATE
-- Drop (delete) the database called demo by typing the following
DROP DATABASE demo;

--SHOW DATABASES to verify deletion
